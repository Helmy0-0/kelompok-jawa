package pertemuan4.hall18;

public class Matematika {
    public int pertambahan(int a, int b) {
        return a + b;
    }

    public int perkalian(int a, int b) {
        return a * b;
    }

    public int kurang(int a, int b) {
        return a - b;
    }

    public  int bagi (int a, int b) {
        return a / b;
    }

}
